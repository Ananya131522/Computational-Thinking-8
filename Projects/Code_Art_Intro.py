# ### SETUP ###
import turtle
# ###############################################

t = turtle.Turtle()
t.penup()
t.goto(-100, -100)
t.color("purple")
t.pendown()

for i in range(1000000000):
    t.forward(5)
    t.left(144)
# ###############################################
# ### ENDING ###
turtle.exitonclick()
