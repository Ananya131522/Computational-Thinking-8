t.goto(-100, -100)
t.color("green")
t.pendown()

t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)




# ###############################################
# ### ENDING ###
turtle.exitonclick()
